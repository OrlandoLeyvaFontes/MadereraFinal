    private void cargarMaderasEnTablas() {
        List<EntradasDTO> maderaLista = this.iEntradaSS.obtenerMaderas();
        if (maderaLista != null && !maderaLista.isEmpty()) {
            llenarTablaMaderas(maderaLista);
        } else {
            JOptionPane.showMessageDialog(this, "No hay datos disponibles para mostrar.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void llenarTablaMaderas(List<EntradasDTO> entradaLista) {
        DefaultTableModel model = new DefaultTableModel(
                new String[]{"Tipo Entrada", "Tipo Madera", "Cantidad", "Fecha"}, 0
        ) {
            boolean[] canEdit = new boolean[]{false, false, false, false};  // Hace que las celdas no sean editables.

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };
        
        for (EntradasDTO entrada : entradaLista) {
            model.addRow(new Object[]{
                entrada.getTipoEntrada(),
                entrada.getTipoMadera(),
                entrada.getCantidad(),
                entrada.getFechaEntrada(),});

        }

        tblEntradas.setModel(model);
    }

